int
cmdline_parser_required (struct gengetopt_args_info *args_info, const char *prog_name)
{
  int result = EXIT_SUCCESS;

  if (cmdline_parser_required2(args_info, prog_name, 0) > 0)
    result = EXIT_FAILURE;

  if (result == EXIT_FAILURE)
    {
      cmdline_parser_free (args_info);
      exit (EXIT_FAILURE);
    }
  
  return result;
}

int
cmdline_parser_required2 (struct gengetopt_args_info *args_info, const char *prog_name, const char *additional_error)
{
  int error_occurred = 0;
  FIX_UNUSED (additional_error);

  /* checks for required options */
  if (check_multiple_option_occurrences(prog_name, args_info->verbose_given, args_info->verbose_min, args_info->verbose_max, "'--verbose' ('-v')"))
     error_occurred = 1;
  
  if (check_multiple_option_occurrences(prog_name, args_info->source_given, args_info->source_min, args_info->source_max, "'--source' ('-s')"))
     error_occurred = 1;
  
  if (check_multiple_option_occurrences(prog_name, args_info->repair_given, args_info->repair_min, args_info->repair_max, "'--repair' ('-r')"))
     error_occurred = 1;
  
  if (check_multiple_option_occurrences(prog_name, args_info->control_given, args_info->control_min, args_info->control_max, "'--control' ('-c')"))
     error_occurred = 1;
  
  
  /* checks for dependences among options */

  return error_occurred;
}


static char *package_name = 0;

/**
 * @brief updates an option
 * @param field the generic pointer to the field to update
 * @param orig_field the pointer to the orig field
 * @param field_given the pointer to the number of occurrence of this option
 * @param prev_given the pointer to the number of occurrence already seen
 * @param value the argument for this option (if null no arg was specified)
 * @param possible_values the possible values for this option (if specified)
 * @param default_value the default value (in case the option only accepts fixed values)
 * @param arg_type the type of this option
 * @param check_ambiguity @see cmdline_parser_params.check_ambiguity
 * @param override @see cmdline_parser_params.override
 * @param no_free whether to free a possible previous value
 * @param multiple_option whether this is a multiple option
 * @param long_opt the corresponding long option
 * @param short_opt the corresponding short option (or '-' if none)
 * @param additional_error possible further error specification
 */
static
int update_arg(void *field, char **orig_field,
               unsigned int *field_given, unsigned int *prev_given, 
               char *value, const char *possible_values[],
               const char *default_value,
               cmdline_parser_arg_type arg_type,
               int check_ambiguity, int override,
               int no_free, int multiple_option,
               const char *long_opt, char short_opt,
               const char *additional_error)
{
  char *stop_char = 0;
  const char *val = value;
  int found;
  char **string_field;
  FIX_UNUSED (field);

  stop_char = 0;
  found = 0;

  if (!multiple_option && prev_given && (*prev_given || (check_ambiguity && *field_given)))
    {
      if (short_opt != '-')
        fprintf (stderr, "%s: `--%s' (`-%c') option given more than once%s\n", 
               package_name, long_opt, short_opt,
               (additional_error ? additional_error : ""));
      else
        fprintf (stderr, "%s: `--%s' option given more than once%s\n", 
               package_name, long_opt,
               (additional_error ? additional_error : ""));
      return 1; /* failure */
    }

  if (possible_values && (found = check_possible_values((value ? value : default_value), possible_values)) < 0)
    {
      if (short_opt != '-')
        fprintf (stderr, "%s: %s argument, \"%s\", for option `--%s' (`-%c')%s\n", 
          package_name, (found == -2) ? "ambiguous" : "invalid", value, long_opt, short_opt,
          (additional_error ? additional_error : ""));
      else
        fprintf (stderr, "%s: %s argument, \"%s\", for option `--%s'%s\n", 
          package_name, (found == -2) ? "ambiguous" : "invalid", value, long_opt,
          (additional_error ? additional_error : ""));
      return 1; /* failure */
    }
    
  if (field_given && *field_given && ! override)
    return 0;
  if (prev_given)
    (*prev_given)++;
  if (field_given)
    (*field_given)++;
  if (possible_values)
    val = possible_values[found];

  switch(arg_type) {
  case ARG_FLAG:
    *((int *)field) = !*((int *)field);
    break;
  case ARG_INT:
    if (val) *((int *)field) = strtol (val, &stop_char, 0);
    break;
  case ARG_ENUM:
    if (val) *((int *)field) = found;
    break;
  case ARG_STRING:
    if (val) {
      string_field = (char **)field;
      if (!no_free && *string_field)
        free (*string_field); /* free previous string */
      *string_field = gengetopt_strdup (val);
    }
    break;
  default:
    break;
  };

  /* check numeric conversion */
  switch(arg_type) {
  case ARG_INT:
    if (val && !(stop_char && *stop_char == '\0')) {
      fprintf(stderr, "%s: invalid numeric value: %s\n", package_name, val);
      return 1; /* failure */
    }
    break;
  default:
    ;
  };

  /* store the original value */
  switch(arg_type) {
  case ARG_NO:
  case ARG_FLAG:
    break;
  default:
    if (value && orig_field) {
      if (no_free) {
        *orig_field = value;
      } else {
        if (*orig_field)
          free (*orig_field); /* free previous string */
        *orig_field = gengetopt_strdup (value);
      }
    }
  };

  return 0; /* OK */
}

/**
 * @brief store information about a multiple option in a temporary list
 * @param list where to (temporarily) store multiple options
 */
static
int update_multiple_arg_temp(struct generic_list **list,
               unsigned int *prev_given, const char *val,
               const char *possible_values[], const char *default_value,
               cmdline_parser_arg_type arg_type,
               const char *long_opt, char short_opt,
               const char *additional_error)
{
  /* store single arguments */
  char *multi_token;
  const char *multi_next;

  if (arg_type == ARG_NO) {
    (*prev_given)++;
    return 0; /* OK */
  }

  multi_token = get_multiple_arg_token(val);
  multi_next = get_multiple_arg_token_next (val);

  while (1)
    {
      add_node (list);
      if (update_arg((void *)&((*list)->arg), &((*list)->orig), 0,
          prev_given, multi_token, possible_values, default_value, 
          arg_type, 0, 1, 1, 1, long_opt, short_opt, additional_error)) {
        if (multi_token) free(multi_token);
        return 1; /* failure */
      }

      if (multi_next)
        {
          multi_token = get_multiple_arg_token(multi_next);
          multi_next = get_multiple_arg_token_next (multi_next);
        }
      else
        break;
    }

  return 0; /* OK */
}

/**
 * @brief free the passed list (including possible string argument)
 */
static
void free_list(struct generic_list *list, short string_arg)
{
  if (list) {
    struct generic_list *tmp;
    while (list)
      {
        tmp = list;
        if (string_arg && list->arg.string_arg)
          free (list->arg.string_arg);
        if (list->orig)
          free (list->orig);
        list = list->next;
        free (tmp);
      }
  }
}

/**
 * @brief updates a multiple option starting from the passed list
 */
static
void update_multiple_arg(void *field, char ***orig_field,
               unsigned int field_given, unsigned int prev_given, union generic_value *default_value,
               cmdline_parser_arg_type arg_type,
               struct generic_list *list)
{
  int i;
  struct generic_list *tmp;

  if (prev_given && list) {
    *orig_field = (char **) realloc (*orig_field, (field_given + prev_given) * sizeof (char *));

    switch(arg_type) {
    case ARG_INT:
    case ARG_ENUM:
      *((int **)field) = (int *)realloc (*((int **)field), (field_given + prev_given) * sizeof (int)); break;
    case ARG_STRING:
      *((char ***)field) = (char **)realloc (*((char ***)field), (field_given + prev_given) * sizeof (char *)); break;
    default:
      break;
    };
    
    for (i = (prev_given - 1); i >= 0; --i)
      {
        tmp = list;
        
        switch(arg_type) {
        case ARG_INT:
          (*((int **)field))[i + field_given] = tmp->arg.int_arg; break;
        case ARG_ENUM:
          (*((int **)field))[i + field_given] = tmp->arg.int_arg; break;
        case ARG_STRING:
          (*((char ***)field))[i + field_given] = tmp->arg.string_arg; break;
        default:
          break;
        }        
        (*orig_field) [i + field_given] = list->orig;
        list = list->next;
        free (tmp);
      }
  } else { /* set the default value */
    if (default_value && ! field_given) {
      switch(arg_type) {
      case ARG_INT:
      case ARG_ENUM:
        if (! *((int **)field)) {
          *((int **)field) = (int *)malloc (sizeof (int));
          (*((int **)field))[0] = default_value->int_arg; 
        }
        break;
      case ARG_STRING:
        if (! *((char ***)field)) {
          *((char ***)field) = (char **)malloc (sizeof (char *));
          (*((char ***)field))[0] = gengetopt_strdup(default_value->string_arg);
        }
        break;
      default: break;
      }
      if (!(*orig_field)) {
        *orig_field = (char **) malloc (sizeof (char *));
        (*orig_field)[0] = 0;
      }
    }
  }
}

int
cmdline_parser_internal (
  int argc, char **argv, struct gengetopt_args_info *args_info,
                        struct cmdline_parser_params *params, const char *additional_error)
{
  int c;	/* Character of the parsed option.  */

  struct generic_list * source_list = NULL;
  struct generic_list * repair_list = NULL;
  struct generic_list * control_list = NULL;
  int error_occurred = 0;
  struct gengetopt_args_info local_args_info;
  
  int override;
  int initialize;
  int check_required;
  int check_ambiguity;
  
  package_name = argv[0];
  
  /* TODO: Why is this here? It is not used anywhere. */
  override = params->override;
  FIX_UNUSED(override);

  initialize = params->initialize;
  check_required = params->check_required;

  /* TODO: Why is this here? It is not used anywhere. */
  check_ambiguity = params->check_ambiguity;
  FIX_UNUSED(check_ambiguity);

  if (initialize)
    cmdline_parser_init (args_info);

  cmdline_parser_init (&local_args_info);

  optarg = 0;
  optind = 0;
  opterr = params->print_errors;
  optopt = '?';

  while (1)
    {
      int option_index = 0;

      static struct option long_options[] = {
        { "help",	0, NULL, 'h' },
        { "version",	0, NULL, 'V' },
        { "verbose",	0, NULL, 'v' },
        { "list-supported",	0, NULL, 'L' },
        { "input",	1, NULL, 'i' },
        { "input-format",	1, NULL, 0 },
        { "source",	1, NULL, 's' },
        { "repair",	1, NULL, 'r' },
        { "control",	1, NULL, 'c' },
        { "reuseaddr",	0, NULL, 0 },
        { "target-latency",	1, NULL, 0 },
        { "io-latency",	1, NULL, 0 },
        { "latency-tolerance",	1, NULL, 0 },
        { "nbsrc",	1, NULL, 0 },
        { "nbrpr",	1, NULL, 0 },
        { "packet-len",	1, NULL, 0 },
        { "frame-len",	1, NULL, 0 },
        { "max-packet-size",	1, NULL, 0 },
        { "max-frame-size",	1, NULL, 0 },
        { "rate",	1, NULL, 0 },
        { "latency-backend",	1, NULL, 0 },
        { "latency-profile",	1, NULL, 0 },
        { "resampler-backend",	1, NULL, 0 },
        { "resampler-profile",	1, NULL, 0 },
        { "interleaving",	0, NULL, 0 },
        { "profiling",	0, NULL, 0 },
        { "color",	1, NULL, 0 },
        { "no-resampling",	0, NULL, 0 },
        { 0,  0, 0, 0 }
      };

      c = getopt_long (argc, argv, "hVvLi:s:r:c:", long_options, &option_index);

      if (c == -1) break;	/* Exit from `while (1)' loop.  */

      switch (c)
        {
        case 'h':	/* Print help and exit.  */
          cmdline_parser_print_help ();
          cmdline_parser_free (&local_args_info);
          exit (EXIT_SUCCESS);

        case 'V':	/* Print version and exit.  */
          cmdline_parser_print_version ();
          cmdline_parser_free (&local_args_info);
          exit (EXIT_SUCCESS);

        case 'v':	/* Increase verbosity level (may be used multiple times).  */
        
          local_args_info.verbose_given++;
        
          break;
        case 'L':	/* list supported schemes and formats.  */
        
        
          if (update_arg( 0 , 
               0 , &(args_info->list_supported_given),
              &(local_args_info.list_supported_given), optarg, 0, 0, ARG_NO,
              check_ambiguity, override, 0, 0,
              "list-supported", 'L',
              additional_error))
            goto failure;
        
          break;
        case 'i':	/* Input file or device URI.  */
        
        
          if (update_arg( (void *)&(args_info->input_arg), 
               &(args_info->input_orig), &(args_info->input_given),
              &(local_args_info.input_given), optarg, 0, 0, ARG_STRING,
              check_ambiguity, override, 0, 0,
              "input", 'i',
              additional_error))
            goto failure;
        
          break;
        case 's':	/* Remote source endpoint.  */
        
          if (update_multiple_arg_temp(&source_list, 
              &(local_args_info.source_given), optarg, 0, 0, ARG_STRING,
              "source", 's',
              additional_error))
            goto failure;
        
          break;
        case 'r':	/* Remote repair endpoint.  */
        
          if (update_multiple_arg_temp(&repair_list, 
              &(local_args_info.repair_given), optarg, 0, 0, ARG_STRING,
              "repair", 'r',
              additional_error))
            goto failure;
        
          break;
        case 'c':	/* Remote control endpoint.  */
        
          if (update_multiple_arg_temp(&control_list, 
              &(local_args_info.control_given), optarg, 0, 0, ARG_STRING,
              "control", 'c',
              additional_error))
            goto failure;
        
          break;

        case 0:	/* Long option with no short option */
          /* Force input file format.  */
          if (strcmp (long_options[option_index].name, "input-format") == 0)
          {
          
          
            if (update_arg( (void *)&(args_info->input_format_arg), 
                 &(args_info->input_format_orig), &(args_info->input_format_given),
                &(local_args_info.input_format_given), optarg, 0, 0, ARG_STRING,
                check_ambiguity, override, 0, 0,
                "input-format", '-',
                additional_error))
              goto failure;
          
          }
          /* enable SO_REUSEADDR when binding sockets.  */
          else if (strcmp (long_options[option_index].name, "reuseaddr") == 0)
          {
          
          
            if (update_arg( 0 , 
                 0 , &(args_info->reuseaddr_given),
                &(local_args_info.reuseaddr_given), optarg, 0, 0, ARG_NO,
                check_ambiguity, override, 0, 0,
                "reuseaddr", '-',
                additional_error))
              goto failure;
          
          }
          /* Target latency, TIME units.  */
          else if (strcmp (long_options[option_index].name, "target-latency") == 0)
          {
          
          
            if (update_arg( (void *)&(args_info->target_latency_arg), 
                 &(args_info->target_latency_orig), &(args_info->target_latency_given),
                &(local_args_info.target_latency_given), optarg, 0, 0, ARG_STRING,
                check_ambiguity, override, 0, 0,
                "target-latency", '-',
                additional_error))
              goto failure;
          
          }
          /* Recording target latency, TIME units.  */
          else if (strcmp (long_options[option_index].name, "io-latency") == 0)
          {
          
          
            if (update_arg( (void *)&(args_info->io_latency_arg), 
                 &(args_info->io_latency_orig), &(args_info->io_latency_given),
                &(local_args_info.io_latency_given), optarg, 0, 0, ARG_STRING,
                check_ambiguity, override, 0, 0,
                "io-latency", '-',
                additional_error))
              goto failure;
          
          }
          /* Maximum deviation from target latency, TIME units.  */
          else if (strcmp (long_options[option_index].name, "latency-tolerance") == 0)
          {
          
          
            if (update_arg( (void *)&(args_info->latency_tolerance_arg), 
                 &(args_info->latency_tolerance_orig), &(args_info->latency_tolerance_given),
                &(local_args_info.latency_tolerance_given), optarg, 0, 0, ARG_STRING,
                check_ambiguity, override, 0, 0,
                "latency-tolerance", '-',
                additional_error))
              goto failure;
          
          }
          /* Number of source packets in FEC block.  */
          else if (strcmp (long_options[option_index].name, "nbsrc") == 0)
          {
          
          
            if (update_arg( (void *)&(args_info->nbsrc_arg), 
                 &(args_info->nbsrc_orig), &(args_info->nbsrc_given),
                &(local_args_info.nbsrc_given), optarg, 0, 0, ARG_INT,
                check_ambiguity, override, 0, 0,
                "nbsrc", '-',
                additional_error))
              goto failure;
          
          }
          /* Number of repair packets in FEC block.  */
          else if (strcmp (long_options[option_index].name, "nbrpr") == 0)
          {
          
          
            if (update_arg( (void *)&(args_info->nbrpr_arg), 
                 &(args_info->nbrpr_orig), &(args_info->nbrpr_given),
                &(local_args_info.nbrpr_given), optarg, 0, 0, ARG_INT,
                check_ambiguity, override, 0, 0,
                "nbrpr", '-',
                additional_error))
              goto failure;
          
          }
          /* Outgoing packet length, TIME units.  */
          else if (strcmp (long_options[option_index].name, "packet-len") == 0)
          {
          
          
            if (update_arg( (void *)&(args_info->packet_len_arg), 
                 &(args_info->packet_len_orig), &(args_info->packet_len_given),
                &(local_args_info.packet_len_given), optarg, 0, 0, ARG_STRING,
                check_ambiguity, override, 0, 0,
                "packet-len", '-',
                additional_error))
              goto failure;
          
          }
          /* Duration of the internal frames, TIME units.  */
          else if (strcmp (long_options[option_index].name, "frame-len") == 0)
          {
          
          
            if (update_arg( (void *)&(args_info->frame_len_arg), 
                 &(args_info->frame_len_orig), &(args_info->frame_len_given),
                &(local_args_info.frame_len_given), optarg, 0, 0, ARG_STRING,
                check_ambiguity, override, 0, 0,
                "frame-len", '-',
                additional_error))
              goto failure;
          
          }
          /* Maximum packet size, in SIZE units.  */
          else if (strcmp (long_options[option_index].name, "max-packet-size") == 0)
          {
          
          
            if (update_arg( (void *)&(args_info->max_packet_size_arg), 
                 &(args_info->max_packet_size_orig), &(args_info->max_packet_size_given),
                &(local_args_info.max_packet_size_given), optarg, 0, 0, ARG_STRING,
                check_ambiguity, override, 0, 0,
                "max-packet-size", '-',
                additional_error))
              goto failure;
          
          }
          /* Maximum internal frame size, in SIZE units.  */
          else if (strcmp (long_options[option_index].name, "max-frame-size") == 0)
          {
          
          
            if (update_arg( (void *)&(args_info->max_frame_size_arg), 
                 &(args_info->max_frame_size_orig), &(args_info->max_frame_size_given),
                &(local_args_info.max_frame_size_given), optarg, 0, 0, ARG_STRING,
                check_ambiguity, override, 0, 0,
                "max-frame-size", '-',
                additional_error))
              goto failure;
          
          }
          /* Override input sample rate, Hz.  */
          else if (strcmp (long_options[option_index].name, "rate") == 0)
          {
          
          
            if (update_arg( (void *)&(args_info->rate_arg), 
                 &(args_info->rate_orig), &(args_info->rate_given),
                &(local_args_info.rate_given), optarg, 0, 0, ARG_INT,
                check_ambiguity, override, 0, 0,
                "rate", '-',
                additional_error))
              goto failure;
          
          }
          /* Which latency to use in latency tuner.  */
          else if (strcmp (long_options[option_index].name, "latency-backend") == 0)
          {
          
          
            if (update_arg( (void *)&(args_info->latency_backend_arg), 
                 &(args_info->latency_backend_orig), &(args_info->latency_backend_given),
                &(local_args_info.latency_backend_given), optarg, cmdline_parser_latency_backend_values, "niq", ARG_ENUM,
                check_ambiguity, override, 0, 0,
                "latency-backend", '-',
                additional_error))
              goto failure;
          
          }
          /* Latency tuning profile.  */
          else if (strcmp (long_options[option_index].name, "latency-profile") == 0)
          {
          
          
            if (update_arg( (void *)&(args_info->latency_profile_arg), 
                 &(args_info->latency_profile_orig), &(args_info->latency_profile_given),
                &(local_args_info.latency_profile_given), optarg, cmdline_parser_latency_profile_values, "intact", ARG_ENUM,
                check_ambiguity, override, 0, 0,
                "latency-profile", '-',
                additional_error))
              goto failure;
          
          }
          /* Resampler backend.  */
          else if (strcmp (long_options[option_index].name, "resampler-backend") == 0)
          {
          
          
            if (update_arg( (void *)&(args_info->resampler_backend_arg), 
                 &(args_info->resampler_backend_orig), &(args_info->resampler_backend_given),
                &(local_args_info.resampler_backend_given), optarg, cmdline_parser_resampler_backend_values, "default", ARG_ENUM,
                check_ambiguity, override, 0, 0,
                "resampler-backend", '-',
                additional_error))
              goto failure;
          
          }
          /* Resampler profile.  */
          else if (strcmp (long_options[option_index].name, "resampler-profile") == 0)
          {
          
          
            if (update_arg( (void *)&(args_info->resampler_profile_arg), 
                 &(args_info->resampler_profile_orig), &(args_info->resampler_profile_given),
                &(local_args_info.resampler_profile_given), optarg, cmdline_parser_resampler_profile_values, "medium", ARG_ENUM,
                check_ambiguity, override, 0, 0,
                "resampler-profile", '-',
                additional_error))
              goto failure;
          
          }
          /* Enable packet interleaving.  */
          else if (strcmp (long_options[option_index].name, "interleaving") == 0)
          {
          
          
            if (update_arg((void *)&(args_info->interleaving_flag), 0, &(args_info->interleaving_given),
                &(local_args_info.interleaving_given), optarg, 0, 0, ARG_FLAG,
                check_ambiguity, override, 1, 0, "interleaving", '-',
                additional_error))
              goto failure;
          
          }
          /* Enable self profiling.  */
          else if (strcmp (long_options[option_index].name, "profiling") == 0)
          {
          
          
            if (update_arg((void *)&(args_info->profiling_flag), 0, &(args_info->profiling_given),
                &(local_args_info.profiling_given), optarg, 0, 0, ARG_FLAG,
                check_ambiguity, override, 1, 0, "profiling", '-',
                additional_error))
              goto failure;
          
          }
          /* Set colored logging mode for stderr output.  */
          else if (strcmp (long_options[option_index].name, "color") == 0)
          {
          
          
            if (update_arg( (void *)&(args_info->color_arg), 
                 &(args_info->color_orig), &(args_info->color_given),
                &(local_args_info.color_given), optarg, cmdline_parser_color_values, "auto", ARG_ENUM,
                check_ambiguity, override, 0, 0,
                "color", '-',
                additional_error))
              goto failure;
          
          }
          /* Disable automatic resampling and fail if rates mismatch.  */
          else if (strcmp (long_options[option_index].name, "no-resampling") == 0)
          {
          
          
            if (update_arg((void *)&(args_info->no_resampling_flag), 0, &(args_info->no_resampling_given),
                &(local_args_info.no_resampling_given), optarg, 0, 0, ARG_FLAG,
                check_ambiguity, override, 1, 0, "no-resampling", '-',
                additional_error))
              goto failure;
          
          }
          
          break;
        case '?':	/* Invalid option.  */
          /* `getopt_long' already printed an error message.  */
          goto failure;

        default:	/* bug: option not considered.  */
          fprintf (stderr, "%s: option unknown: %c%s\n", CMDLINE_PARSER_PACKAGE, c, (additional_error ? additional_error : ""));
          abort ();
        } /* switch */
    } /* while */


  update_multiple_arg((void *)&(args_info->source_arg),
    &(args_info->source_orig), args_info->source_given,
    local_args_info.source_given, 0,
    ARG_STRING, source_list);
  update_multiple_arg((void *)&(args_info->repair_arg),
    &(args_info->repair_orig), args_info->repair_given,
    local_args_info.repair_given, 0,
    ARG_STRING, repair_list);
  update_multiple_arg((void *)&(args_info->control_arg),
    &(args_info->control_orig), args_info->control_given,
    local_args_info.control_given, 0,
    ARG_STRING, control_list);

  args_info->verbose_given += local_args_info.verbose_given;
  local_args_info.verbose_given = 0;
  args_info->source_given += local_args_info.source_given;
  local_args_info.source_given = 0;
  args_info->repair_given += local_args_info.repair_given;
  local_args_info.repair_given = 0;
  args_info->control_given += local_args_info.control_given;
  local_args_info.control_given = 0;
  
  if (check_required)
    {
      error_occurred += cmdline_parser_required2 (args_info, argv[0], additional_error);
    }

  cmdline_parser_release (&local_args_info);

  if ( error_occurred )
    return (EXIT_FAILURE);

  return 0;

failure:
  free_list (source_list, 1 );
  free_list (repair_list, 1 );
  free_list (control_list, 1 );
  
  cmdline_parser_release (&local_args_info);
  return (EXIT_FAILURE);
}
